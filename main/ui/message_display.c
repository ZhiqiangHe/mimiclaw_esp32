static void refresh_display(void);

/* Draw a character to line buffer */
static void draw_char_to_buffer(uint16_t *buffer, int x, char c, uint16_t color)
{
    if (x < 0 || x >= LCD_WIDTH - FONT_WIDTH) return;
    if (c < 32 || c > 127) c = '?';
    
    const uint8_t *font = s_font_8x16[c - 32];
    
    for (int row = 0; row < FONT_HEIGHT; row++) {
        uint8_t font_byte = font[row];
        for (int col = 0; col < FONT_WIDTH; col++) {
            if (font_byte & (0x80 >> col)) {
                buffer[row * LCD_WIDTH + x + col] = color;
            }
        }
    }
}

/* Draw text line to buffer */
static void draw_text_line(const char *text, int len, uint16_t fg_color, uint16_t bg_color)
{
    for (int i = 0; i < LCD_WIDTH * FONT_HEIGHT; i++) {
        s_line_buffer[i] = bg_color;
    }
    
    for (int i = 0; i < len && i < CHARS_PER_LINE; i++) {
        draw_char_to_buffer(s_line_buffer, i * FONT_WIDTH, text[i], fg_color);
    }
}

/* Clear screen */
